class Solution:
    def findLength(self, nums1: List[int], nums2: List[int]) -> int:
        a = len(nums1)
        b = len(nums2)
        c = [[0] * (b + 1) for i in range(a + 1)]
        d = 0
        for i in range(a - 1, -1, -1):
            for j in range(b - 1, -1, -1):
                c[i][j] = c[i + 1][j + 1] + 1 if nums1[i] == nums2[j] else 0
                d = max(d, c[i][j])
        return d


